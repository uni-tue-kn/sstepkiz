/**
 * Default time in ms, a client has to authenticate.
 */
export const DEFAULT_AUTH_TIMEOUT = 10000; // 10 seconds.

/**
 * Default time in ms to wait for call response.
 */
export const DEFAULT_CALL_REQUEST_TIMEOUT = 5000; // 5 seconds.

/**
 * Default time in ms to establish a peer connection.
 */
export const DEFAULT_CONNECTION_TIMEOUT = 5000; // 5 seconds.

/**
 * Default number of reconnects to signalling server.
 */
export const DEFAULT_RECONNECT_COUNT = 10;

/**
 * Default number of ms to wait before trying to reconnect to signalling server.
 */
export const DEFAULT_RECONNECT_TIMEOUT = 1000; // 1 second.

/**
 * Default time in ms to wait for registration response.
 */
export const DEFAULT_REGISTRATION_TIMEOUT = 5000; // 5 seconds.

/**
 * Default time in ms to wait before call is declined automatically.
 */
export const DEFAULT_RING_TIMEOUT = 30000; // 30 seconds.
